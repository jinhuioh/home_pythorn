#연산자(operator)
#연산을 해주는 것, 간단한 연산을 해주는 기호!
# +, -, ==(equl), =(대입연산자) 등등
#산술연산자(사칙연산자) : + - * /(나누기:나머지0) //(나누기:나머지 소수점 잘라버림)
#  %(나머지)

n1 = 222
n2 = 100

print(n1, '과', n2, '의 합은', n1+n2)
print(n1, '과', n2, '의 빼기는', n1-n2)
print(n1, '과', n2, '의 곱은', n1*n2)
print(n1, '과', n2, '의 나누기는(/)', n1/n2)
print(n1, '과', n2, '의 나누기는(//)', n1//n2)
print(n1, '과', n2, '의 %는', n1%n2)

